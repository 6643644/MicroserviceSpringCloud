package com.pattern.composite.receiver;

public class Calculator {

    public int plus(int op1, int op2) {

	return op1 + op2;
    }

    public int minus(int op1, int op2) {

	return op1 - op2;
    }

    public double divide(int op1, int op2) {

	return op1 / op2;
    }

    public int multiply(int op1, int op2) {

	return op1 * op2;
    }

}
